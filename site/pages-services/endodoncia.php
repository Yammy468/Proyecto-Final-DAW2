                <h4>Endodoncias multirradiculares</h4>
                <p>Son las endodoncias realizadas a aquellos dientes que cuentan con tres o más conductos radiculares. Los molares superiores e inferiores suelen contar con cuatro conductos radiculares, ya que suelen presentar dos canales en su raíz mesial.</p> <br>
