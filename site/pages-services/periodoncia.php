                <h3>Tratamiento</h3>
                <p>El objetivo del tratamiento es <strong>limpiar exhaustivamente las bolsas</strong> alrededor de los dientes y <strong>prevenir daño</strong> en las encías y el hueso que las rodea. Las mejores posibilidades de éxito en el tratamiento se obtienen cuando se lleva una rutina diaria de buen cuidado bucal, se controlan las enfermedades que puedan tener repercusión en la salud dental y se deja de consumir tabaco.</p> <br>

                <h4>Tratamientos no quirúrgicos</h4>
                <p>Si la periodontitis no está avanzada, el tratamiento puede consistir en procedimientos menos invasivos, incluidos:</p> <br>
                <ul>
                  <li><strong>Raspado.</strong> El raspado elimina el sarro dental y las bacterias de la superficie de los dientes y debajo de la línea de las encías. Puede realizarse con instrumentos, un láser o un dispositivo ultrasónico.</li>
                  <li><strong>Alisado radicular.</strong> El alisado radicular suaviza la superficie de las raíces. Esto ayuda a prevenir que se sigan acumulando sarro dental y bacterias. También ayuda a que las encías se vuelvan a conectar a los dientes.</li>
                  <li><strong>Antibióticos.</strong> Los antibióticos tópicos u orales pueden ayudar a controlar la infección bacteriana. Los antibióticos tópicos pueden incluir enjuagues bucales con antibiótico o geles con antibiótico que se colocan en las bolsas de las encías. Algunas veces, los antibióticos orales son necesarios para combatir las bacterias que provocan infecciones.</li>
                </ul> <br>

                <h4>Tratamientos quirúrgicos</h4>
                <p>Si tienes periodontitis avanzada, es posible que necesites una cirugía dental, por ejemplo:</p> <br>
                <ul>
                  <li><strong>Cirugía con colgajos, también llamada cirugía de reducción de la bolsa periodontal.</strong> El periodoncista hace cortes en las encías para plegar cuidadosamente el tejido. Esto expone las raíces de los dientes para un mejor raspado y alisado radicular. Debido a que la periodontitis suele causar pérdida ósea, el hueso subyacente se puede volver a moldear antes de suturar el tejido gingival nuevamente en su lugar. Una vez que te recuperes, será más fácil limpiar las áreas alrededor de los dientes y mantener sano el tejido gingival.</li>
                  <li><strong>Injertos de tejido blando.</strong> Cuando pierdes tejido gingival, la línea de la encía retrocede, lo cual expone parte de las raíces de los dientes. Es posible que haya que reforzar parte del tejido dañado. Por lo general, esto se hace extrayendo una pequeña cantidad de tejido del paladar o utilizando tejido de otro lugar y fijándolo al lugar afectado. Esto puede ayudar a reducir la pérdida de tejido gingival, cubrir las raíces expuestas y dar a los dientes una mejor apariencia.</li>
                  <li><strong>Injerto óseo.</strong> Este procedimiento se realiza cuando la periodontitis destruye el hueso que rodea la raíz del diente. El injerto puede estar compuesto por pequeños fragmentos de tu propio hueso, o bien, el hueso puede ser de material sintético o donado. El injerto óseo ayuda a prevenir la pérdida del diente al sostenerlo en su lugar. También sirve como plataforma para el nuevo crecimiento del hueso natural.</li>
                  <li><strong>Regeneración tisular guiada.</strong> Esto permite que el hueso destruido por las bacterias vuelva a crecer. Una de las técnicas consiste en que el dentista coloque un tipo especial de tela entre el hueso existente y el diente. El material evita que crezca tejido no deseado en el área en recuperación, lo que más bien permite que el hueso vuelva a crecer.</li>
                  <li><strong>Proteínas estimulantes de tejidos.</strong> Otra técnica incluye la aplicación de un gel especial a la raíz del diente afectado. Este gel contiene las mismas proteínas que se encuentran en el esmalte de los dientes en desarrollo y estimula el crecimiento de huesos y tejidos saludables.</li>
                </ul>
