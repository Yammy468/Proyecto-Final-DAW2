                <h3>¿Qué es una limpieza dental profesional?</h3>
                <p>Una limpieza dental profesional, tambén conocido como profilaxis, es un procedimiento dental en el que un dentista o higienista dental limpia y elimina la placa y el sarro de tus dientes y encías. La placa es una película pegajosa de bacterias que se acumula constantemente en nuestros dientes y encías, y si no se elimina, puede endurecerse y convertirse en sarro. El sarro es una acumulación dura y calcificada de placa que solo se puede eliminar con herramientas dentales especiales.</p>
